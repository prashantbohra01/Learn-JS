// While loop

let i = 0
while (i <= 10) {
    console.log(`Value of i is ${i}`);
    i = i + 2  // condition
}

// While loop for array

let myArr = ["Flash", "Batman", "Superman"]
let arr = 0

while (arr < myArr.length) {
    console.log(`Value is ${myArr[arr]}`);
    arr = arr + 1
    
}


// DO-WHILE Loop 

let score = 11
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);


